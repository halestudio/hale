package eu.esdihumboldt.hale.ui.common.graph.content;

import net.jcip.annotations.Immutable;
import eu.esdihumboldt.util.Pair;

/**
 * Represents an edge in a cell/entity graph.
 */
@Immutable
class Edge extends Pair<Object, Object> {

	private final String name;

	/**
	 * Create an edge
	 * 
	 * @param first the source node
	 * @param second the destination node
	 * @param name the edge name, may be <code>null</code>
	 */
	public Edge(Object first, Object second, String name) {
		super(first, second);
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @see Object#toString()
	 */
	@Override
	public String toString() {
		return getName();
	}

	/**
	 * @see Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	/**
	 * @see Object#equals(Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		Edge other = (Edge) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		}
		else if (!name.equals(other.name))
			return false;
		return true;
	}

}