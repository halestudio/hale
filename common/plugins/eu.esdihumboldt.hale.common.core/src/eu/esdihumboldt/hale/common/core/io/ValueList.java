package eu.esdihumboldt.hale.common.core.io;

import java.util.ArrayList;
import java.util.Collection;

/**
 * A list of {@link Value}s that can itself be represented as complex value.
 * 
 * @author Simon Templer
 */
public class ValueList extends ArrayList<Value> {

	private static final long serialVersionUID = -3644067771383146412L;

	/**
	 * @see ArrayList#ArrayList()
	 */
	public ValueList() {
		super();
	}

	/**
	 * @see ArrayList#ArrayList(Collection)
	 */
	public ValueList(Collection<? extends Value> c) {
		super(c);
	}

	/**
	 * @see ArrayList#ArrayList(int)
	 */
	public ValueList(int initialCapacity) {
		super(initialCapacity);
	}

}
